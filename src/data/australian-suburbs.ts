const MATTHEW_PROCTOR_JSON_URL =
  "https://raw.githubusercontent.com/matthewproctor/australianpostcodes/master/australian_postcodes.json";

/** Session cache for Matthew Proctor data - fetched once, reused for all subsequent searches */
let matthewProctorCache: SuburbData[] | null = null;

interface MatthewProctorEntry {
  locality?: string;
  state?: string;
  postcode?: string;
}

/** Fetches Matthew Proctor postcode data, maps to SuburbData, dedupes, and caches */
async function fetchMatthewProctorSuburbs(): Promise<SuburbData[]> {
  if (matthewProctorCache) return matthewProctorCache;

  const response = await fetch(MATTHEW_PROCTOR_JSON_URL);
  if (!response.ok) throw new Error(`Failed to fetch postcodes: ${response.status}`);

  const raw: MatthewProctorEntry[] = await response.json();
  const seen = new Set<string>();
  const suburbs: SuburbData[] = [];

  for (const entry of raw) {
    const locality = (entry.locality ?? "").trim();
    const state = (entry.state ?? "").trim();
    const postcode = String(entry.postcode ?? "").trim();
    if (!locality || !state || !postcode) continue;

    const key = `${locality}|${state}|${postcode}`;
    if (seen.has(key)) continue;
    seen.add(key);

    suburbs.push({ name: locality, state, postcode });
  }

  matthewProctorCache = suburbs;
  return suburbs;
}

function searchInSuburbs(suburbs: SuburbData[], searchTerm: string): SuburbData[] {
  return suburbs.filter(
    (suburb) =>
      suburb.name.toLowerCase().includes(searchTerm) ||
      suburb.state.toLowerCase().includes(searchTerm) ||
      suburb.postcode.includes(searchTerm)
  );
}

/** Async suburb search: AUSTRALIAN_SUBURBS first, then Matthew Proctor (cached per session) */
export async function searchSuburbsAsync(query: string): Promise<SuburbData[]> {
  const searchTerm = query.toLowerCase().trim();
  if (!searchTerm) return AUSTRALIAN_SUBURBS;

  const localResults = searchInSuburbs(AUSTRALIAN_SUBURBS, searchTerm);
  if (localResults.length > 0) return localResults;

  const mpSuburbs = await fetchMatthewProctorSuburbs();
  return searchInSuburbs(mpSuburbs, searchTerm);
}

// Helper function to get all unique suburbs (just the names)
export const getAllSuburbNames = (): string[] => {
  return Array.from(new Set(AUSTRALIAN_SUBURBS.map(s => s.name))).sort();
};

// Helper function to search suburbs (sync, local list only)
export const searchSuburbs = (query: string): SuburbData[] => {
  const searchTerm = query.toLowerCase().trim();
  if (!searchTerm) return AUSTRALIAN_SUBURBS;
  return searchInSuburbs(AUSTRALIAN_SUBURBS, searchTerm);
};

// Helper function to get suburbs by state
export const getSuburbsByState = (state: string): SuburbData[] => {
  return AUSTRALIAN_SUBURBS.filter(suburb => suburb.state === state);
};

